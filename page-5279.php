<h3 class="alignwide has-white-background-color has-background"><strong>BCPS Learning Principles</strong></h3>
<!-- /wp:heading -->

<!-- wp:columns {"align":"wide","style":{"spacing":{"padding":{"top":"80px","right":"80px","bottom":"80px","left":"80px"}}},"backgroundColor":"white"} -->
<div class="wp-block-columns alignwide has-white-background-color has-background" style="padding-top:80px;padding-right:80px;padding-bottom:80px;padding-left:80px"><!-- wp:column {"width":"100%"} -->
<div class="wp-block-column" style="flex-basis:100%"><!-- wp:columns -->
<div class="wp-block-columns"><!-- wp:column {"width":"10%"} -->
<div class="wp-block-column" style="flex-basis:10%"><!-- wp:image {"align":"center","id":5320,"width":69,"height":68,"sizeSlug":"full","linkDestination":"none"} -->
<figure class="wp-block-image aligncenter size-full is-resized"><img src="https://learningcentre.gww.gov.bc.ca/learninghub/wp-content/uploads/sites/6/2023/02/icon-person-single.png" alt="" class="wp-image-5320" width="69" height="68"/></figure>
<!-- /wp:image --></div>
<!-- /wp:column -->

<!-- wp:column {"width":"66.66%"} -->
<div class="wp-block-column" style="flex-basis:66.66%"><!-- wp:paragraph -->
<p><strong>1. Place learners at the center</strong></p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul><!-- wp:list-item -->
<li>Align learning decisions with the needs of learners</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Meet learners where they are through the learning delivery</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Create a learner-centric experience</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Design learning to be accessible</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list --></div>
<!-- /wp:column --></div>
<!-- /wp:columns -->

<!-- wp:columns -->
<div class="wp-block-columns"><!-- wp:column {"width":"10%"} -->
<div class="wp-block-column" style="flex-basis:10%"><!-- wp:image {"align":"center","id":5323,"width":68,"height":71,"sizeSlug":"full","linkDestination":"none"} -->
<figure class="wp-block-image aligncenter size-full is-resized"><img src="https://learningcentre.gww.gov.bc.ca/learninghub/wp-content/uploads/sites/6/2023/02/icon-people.png" alt="" class="wp-image-5323" width="68" height="71"/></figure>
<!-- /wp:image --></div>
<!-- /wp:column -->

<!-- wp:column {"width":"66.66%"} -->
<div class="wp-block-column" style="flex-basis:66.66%"><!-- wp:paragraph -->
<p><strong>2. Align learning with business priorities and goals</strong></p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul><!-- wp:list-item -->
<li>Base learning priorities on business needs</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Base learning decisions on business priorities</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Develop learning topics and courses based on business priorities</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list --></div>
<!-- /wp:column --></div>
<!-- /wp:columns -->

<!-- wp:columns -->
<div class="wp-block-columns"><!-- wp:column {"width":"10%"} -->
<div class="wp-block-column" style="flex-basis:10%"><!-- wp:image {"align":"center","id":5324,"width":69,"height":70,"sizeSlug":"full","linkDestination":"none"} -->
<figure class="wp-block-image aligncenter size-full is-resized"><img src="https://learningcentre.gww.gov.bc.ca/learninghub/wp-content/uploads/sites/6/2023/02/icon-clock.png" alt="" class="wp-image-5324" width="69" height="70"/></figure>
<!-- /wp:image --></div>
<!-- /wp:column -->

<!-- wp:column {"width":"66.66%"} -->
<div class="wp-block-column" style="flex-basis:66.66%"><!-- wp:paragraph -->
<p><strong>3. Make learning available and just-in-time</strong></p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul><!-- wp:list-item -->
<li>Make learning accessible to all kinds of learners in a simple and intuitive way</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Pivot and adapt learning where required, so that it is accessible and available just-in-time</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Balance the development needs of high-quality content with learning that is available at the time of need.</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list --></div>
<!-- /wp:column --></div>
<!-- /wp:columns -->

<!-- wp:columns -->
<div class="wp-block-columns"><!-- wp:column {"width":"10%"} -->
<div class="wp-block-column" style="flex-basis:10%"><!-- wp:image {"align":"center","id":5325,"width":68,"height":70,"sizeSlug":"full","linkDestination":"none"} -->
<figure class="wp-block-image aligncenter size-full is-resized"><img src="https://learningcentre.gww.gov.bc.ca/learninghub/wp-content/uploads/sites/6/2023/02/icon-person-vitruvian.png" alt="" class="wp-image-5325" width="68" height="70"/></figure>
<!-- /wp:image --></div>
<!-- /wp:column -->

<!-- wp:column {"width":"66.66%"} -->
<div class="wp-block-column" style="flex-basis:66.66%"><!-- wp:paragraph -->
<p><strong>4. Empower people to grow their career within the BCPS</strong></p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul><!-- wp:list-item -->
<li>Tie learning to career paths and career journeys that enable learners to grow their career within the BCPS.</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Tie learning to performance development</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list --></div>
<!-- /wp:column --></div>
<!-- /wp:columns -->

<!-- wp:columns -->
<div class="wp-block-columns"><!-- wp:column {"width":"10%"} -->
<div class="wp-block-column" style="flex-basis:10%"><!-- wp:image {"align":"center","id":5323,"width":68,"height":71,"sizeSlug":"full","linkDestination":"none"} -->
<figure class="wp-block-image aligncenter size-full is-resized"><img src="https://learningcentre.gww.gov.bc.ca/learninghub/wp-content/uploads/sites/6/2023/02/icon-people.png" alt="" class="wp-image-5323" width="68" height="71"/></figure>
<!-- /wp:image --></div>
<!-- /wp:column -->

<!-- wp:column {"width":"66.66%"} -->
<div class="wp-block-column" style="flex-basis:66.66%"><!-- wp:paragraph -->
<p><strong>5. Promote connectedness to others and the BCPS desired culture</strong></p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul><!-- wp:list-item -->
<li>Develop learning that enables learners to build social connections within the BCPS and enhance their network.</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Develop learning that enables learners to learn from their peers</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Develop learning that promotes the desired culture within the BCPS</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list --></div>
<!-- /wp:column --></div>
<!-- /wp:columns -->

<!-- wp:columns -->
<div class="wp-block-columns"><!-- wp:column {"width":"10%"} -->
<div class="wp-block-column" style="flex-basis:10%"><!-- wp:image {"align":"center","id":5328,"width":71,"height":71,"sizeSlug":"full","linkDestination":"none"} -->
<figure class="wp-block-image aligncenter size-full is-resized"><img src="https://learningcentre.gww.gov.bc.ca/learninghub/wp-content/uploads/sites/6/2023/02/icon-pie.png" alt="" class="wp-image-5328" width="71" height="71"/></figure>
<!-- /wp:image --></div>
<!-- /wp:column -->

<!-- wp:column {"width":"66.66%"} -->
<div class="wp-block-column" style="flex-basis:66.66%"><!-- wp:paragraph -->
<p><strong>6. Anchor on established content and frameworks</strong></p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul><!-- wp:list-item -->
<li>Anchor learning content on existing corporate frameworks in the BCPS</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Align learning to corporate frameworks that are built on legislation and policies</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Reduce duplication of learning content by avoiding development of already existing content</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list --></div>
<!-- /wp:column --></div>
<!-- /wp:columns -->

<!-- wp:columns -->
<div class="wp-block-columns"><!-- wp:column {"width":"10%"} -->
<div class="wp-block-column" style="flex-basis:10%"><!-- wp:image {"align":"center","id":5329,"width":68,"height":66,"sizeSlug":"full","linkDestination":"none"} -->
<figure class="wp-block-image aligncenter size-full is-resized"><img src="https://learningcentre.gww.gov.bc.ca/learninghub/wp-content/uploads/sites/6/2023/02/icon-bulb.png" alt="" class="wp-image-5329" width="68" height="66"/></figure>
<!-- /wp:image --></div>
<!-- /wp:column -->

<!-- wp:column {"width":"66.66%"} -->
<div class="wp-block-column" style="flex-basis:66.66%"><!-- wp:paragraph -->
<p><strong>7. Encourage active reflection and application to role for learners</strong></p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul><!-- wp:list-item -->
<li>Design learning that engages learners through reflective activities</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Provide opportunities for learners to apply learning to role e.g. project based learning</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list --></div>
<!-- /wp:column --></div>
