<h3 class="alignwide has-white-background-color has-background"><strong>The Corporate Learning Framework is …</strong></h3>
<!-- /wp:heading -->

<!-- wp:paragraph {"backgroundColor":"white","fontSize":"large"} -->
<p class="has-white-background-color has-background has-large-font-size">A modern solution to guide and unify the design, development and delivery of corporate learning for the entire BC Public Service (BCPS). The BCPS is committed to providing aligned, relevant and flexible learning.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"backgroundColor":"white"} -->
<p class="has-white-background-color has-background">It was drafted in consultation with numerous representatives of the BCPS, including our SHR partners and corporate learning partners.</p>
